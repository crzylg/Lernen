for m in MODULES:
    for tp in m["topics"]:
        d = AI_DEEP.get(tp["t"])
        if d:
            tp["lang"] = d.get("lang", [])
            tp["web"] = d.get("web", [])
            deep_count += 1
        else:
            tp["lang"] = tp.get("lang") or []
            tp["web"] = tp.get("web") or []

# ---------------------------------------------------------------------------
# Nummerierung: pro Bereich (Genesys / AI) eigene Schritt-Zaehlung
# ---------------------------------------------------------------------------
total = 0
counters = {"genesys": 0, "ai": 0}
for m in MODULES:
    tr = m["track"]
    for tp in m["topics"]:
        counters[tr] += 1
        tp["nr"] = counters[tr]
        tp["track"] = tr
        total += 1

data = {
    "updated": datetime.date.today().isoformat(),
    "total": total,
    "counts": {"genesys": counters["genesys"], "ai": counters["ai"]},
    "modules": MODULES,
}

out_dir = os.path.join(os.path.dirname(__file__), "..", "app", "data")
out_path = os.path.abspath(os.path.join(out_dir, "curriculum.js"))
with open(out_path, "w", encoding="utf-8") as f:
    f.write("// Automatisch erzeugt von scripts/build_curriculum.py - nicht direkt bearbeiten.\n")
    f.write("window.CURRICULUM = ")
    json.dump(data, f, ensure_ascii=False, indent=1)
    f.write(";\n")

print("Themen gesamt:", total)
print("  davon Genesys:", counters["genesys"], "| AI:", counters["ai"])
print("Module:", len(MODULES))
print("Ausfuehrliche Langtexte (AI_DEEP):", deep_count)
print("Geschrieben:", out_path)
